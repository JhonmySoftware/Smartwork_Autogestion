package com.infotracktest.autogestion.models;

public class FormularioAutogestion {
    private String TipoDocumentos;
    private String NumDocumentos;
    private String CorreoElectronicos;
    private String RazonSocials;
    private String Nombress;
    private String Apellidoss;
    private String TFijos;
    private String NCelulars;

    public String getTipoDocumentos() {
        return TipoDocumentos;
    }

    public void setTipoDocumentos(String tipoDocumentos) {
        TipoDocumentos = tipoDocumentos;
    }

    public String getNumDocumentos() {
        return NumDocumentos;
    }

    public void setNumDocumentos(String numDocumentos) {
        NumDocumentos = numDocumentos;
    }

    public String getCorreoElectronicos() {
        return CorreoElectronicos;
    }

    public void setCorreoElectronicos(String correoElectronicos) {
        CorreoElectronicos = correoElectronicos;
    }

    public String getRazonSocials() {
        return RazonSocials;
    }

    public void setRazonSocials(String razonSocials) {
        RazonSocials = razonSocials;
    }

    public String getNombress() {
        return Nombress;
    }

    public void setNombress(String nombress) {
        Nombress = nombress;
    }

    public String getApellidoss() {
        return Apellidoss;
    }

    public void setApellidoss(String apellidoss) {
        Apellidoss = apellidoss;
    }

    public String getTFijos() {
        return TFijos;
    }

    public void setTFijos(String tFijos) {
        TFijos = tFijos;
    }

    public String getNCelulars() {
        return NCelulars;
    }

    public void setNCelulars(String nCelulars) {
        NCelulars = nCelulars;
    }
}

